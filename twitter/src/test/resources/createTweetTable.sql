
DROP TABLE IF EXISTS TWITTER.TWEETS;

CREATE TABLE TWITTER.TWEETS(
USERNAME VARCHAR(50),
HANDLE VARCHAR(15),
DATEPOSTED VARCHAR(10),
TEXT VARCHAR(280),
IMAGEURL VARCHAR(250),
LARGEIMAGEURL VARCHAR(250),
RETWEETS INT,
LIKES INT,
FOLLOWERS INT,
FOLLOWING INT,
DESCRIPTION VARCHAR(250),
TWEETID VARCHAR(50),
PRIMARY KEY (TWEETID));
